/**
 * _strcpy - copies a string, including the terminating null byte
 * from one pointer to another
 * @dest: the destination pointer
 * @src: the sender pointer
 * Return: the pointer to dest
 */
char *_strcpy(char *dest, char *src)
{
	char *p = dest;

	while (*p++ = *src++)
		return (dest);
}
